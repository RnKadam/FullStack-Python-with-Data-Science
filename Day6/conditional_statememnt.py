#Conditional Statement
# Used to execute a block of code based on a condition

#1) if loop - If condition is true then statement will be executed
#             If condition is false then statement will not be executed

#syntax
# if condition:
#     statement1

#example
a = 10

if a > 5:
    print("a is greater than 5")  # This will be executed since condition is true

#2) if-else loop - If condition is true then statement1 will be executed
#                  If condition is false then statement2 will be executed
#syntax
# if condition:
#     statement1
# else:
#     statement2