#1.Primitive Data Types
#INT
roll_no = 5
print(roll_no)
print(type(roll_no))
print(id(roll_no))

#FLOAT
height = 5.6
print(height)
print(type(height))     #chech data type of variable
print(id(height))  # Print the memory address of 'height'

#STRING
name = "John Doe"
print(name)
print(type(name))      # check data type of variable
print(id(name))  # Print the memory address of 'name'

#BOOLEAN
is_student = True
print(is_student)
print(type(is_student))  # check data type of variable
print(id(is_student))  # Print the memory address of 'is_student'

#COMPLEX
complex_number = 3 + 4j
print(complex_number)
print(type(complex_number))  # check data type of variable


